public class Wand {
    private String name;
    private double power;

    /**
     * @return the name
     */
    public String getName() {
        return this.name;
    }

    /**
     * @return the power
     */
    public double getPower() {
        return this.power;
    }

    /**
     * @param name the name to set
     */
    public void setName(String name) {
        this.name = name;
    }

    /**
     * @param power the power to set
     */
    public void setPower(double power) {
        this.power = power;
    }
}